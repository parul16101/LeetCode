class Solution
{
    public:
    string reverseWord(string str)
    {
        int n = str.size();
        int low = 0, high = n-1;
        
        while(low<=high)
        {
            swap(str[low], str[high]);
            low+=1;
            high-=1;
        }
        return str;
    }
};



